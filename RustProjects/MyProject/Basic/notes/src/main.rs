/// 这是一个函数的文档注释（注意使用了 `///`）。
///
/// # 功能
/// 计算两个数字的和。
///
/// # 参数
/// * `a` - 第一个数字
/// * `b` - 第二个数字
///
/// # 示例 (Examples)
///
/// ```
/// let result = add(2, 3);
/// assert_eq!(result, 5);
/// ```
pub fn add(a: i32, b: i32) -> i32 {
    // 在代码行尾也可以写行注释
    a + b // 返回相加的结果
}

fn main() {
    let x = 5;
    let y = 10;

    // 块注释也可以像这样插在行中间（虽然不常用）
    let z = x + /* 奇怪的注释位置 */ y;

    println!("Result: {}", add(x, z));
}